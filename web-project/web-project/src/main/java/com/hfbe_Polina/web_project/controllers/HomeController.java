package com.hfbe_Polina.web_project.controllers;

import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.GetMapping;

/**
 * Главный контроллер приложения, отвечающий за обработку корневого маршрута.
 * <p>
 * Данный контроллер используется для перенаправления пользователя на основной
 * функциональный раздел приложения — список владельцев. Это позволяет обеспечить
 * корректное поведение при обращении к корневому URL, даже если в проекте
 * отсутствует отдельная главная страница.
 * </p>
 *
 * <p><b>Назначение:</b></p>
 * <ul>
 *     <li>Обработка GET-запроса к корневому маршруту "/"</li>
 *     <li>Перенаправление на страницу списка владельцев</li>
 * </ul>
 *
 * <p><b>Используется в случаях:</b></p>
 * <ul>
 *     <li>Когда пользователь открывает сайт по адресу http://localhost:8080</li>
 *     <li>Когда требуется задать точку входа в приложение</li>
 * </ul>
 */
@Controller
public class HomeController {

    /**
     * Обрабатывает GET-запрос к корневому URL и выполняет перенаправление
     * на страницу списка владельцев.
     *
     * @return строка вида "redirect:/owners", указывающая Spring MVC
     *         выполнить HTTP‑перенаправление на маршрут /owners
     */
    @GetMapping("/")
    public String redirectToOwners() {
        return "redirect:/owners";
    }
}
